function createListItem(str) {
  let decoded = decode(str);
  return React.createElement(
    "li",
    {
      key: decoded
    },
    decoded
  );
}

function decode(input) {
  let doc = new DOMParser().parseFromString(input, "text/html");
  return doc.documentElement.textContent;
}

class List extends React.Component {
  constructor(props) {
    super(props);

    this.state = {
      country: "",
      reversed: false
    };

    this.onChange = this.onChange.bind(this);
    this.reverseList = this.reverseList.bind(this);
  }

  onChange(e) {
    this.setState({
      country: e.target.value.toLowerCase()
    });
  }

  reverseList() {
    country_list.reverse();

    this.setState({
      reversed: !this.state.reversed
    });
  }

  render() {
    const search = React.createElement("input", {
      type: "text",
      onChange: this.onChange
    });

    const reverse = React.createElement(
      "button",
      {
        onClick: this.reverseList
      },
      "Reverse"
    );

    const list = React.createElement(
      "ul",
      {},
      country_list
        .filter(country => country.toLowerCase().includes(this.state.country))
        .map(createListItem)
    );

    return React.createElement(React.Fragment, {}, search, reverse, list);
  }
}

ReactDOM.render(React.createElement(List), document.querySelector("#target"));
